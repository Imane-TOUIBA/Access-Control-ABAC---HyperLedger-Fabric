#!/usr/bin/env bash

function createOrg1() {
  infoln "Enrolling the CA admin"
  mkdir -p organizations/peerOrganizations/cgn.example.com/

  export FABRIC_CA_CLIENT_HOME=${PWD}/organizations/peerOrganizations/cgn.example.com/

  set -x
  fabric-ca-client enroll -u https://admin:adminpw@localhost:7054 --caname ca-cgn --tls.certfiles "${PWD}/organizations/fabric-ca/cgn/ca-cert.pem"
  { set +x; } 2>/dev/null

  echo 'NodeOUs:
  Enable: true
  ClientOUIdentifier:
    Certificate: cacerts/localhost-7054-ca-cgn.pem
    OrganizationalUnitIdentifier: client
  PeerOUIdentifier:
    Certificate: cacerts/localhost-7054-ca-cgn.pem
    OrganizationalUnitIdentifier: peer
  AdminOUIdentifier:
    Certificate: cacerts/localhost-7054-ca-cgn.pem
    OrganizationalUnitIdentifier: admin
  OrdererOUIdentifier:
    Certificate: cacerts/localhost-7054-ca-cgn.pem
    OrganizationalUnitIdentifier: orderer' > "${PWD}/organizations/peerOrganizations/cgn.example.com/msp/config.yaml"

  # Since the CA serves as both the organization CA and TLS CA, copy the org's root cert that was generated by CA startup into the org level ca and tlsca directories

  # Copy cgn's CA cert to cgn's /msp/tlscacerts directory (for use in the channel MSP definition)
  mkdir -p "${PWD}/organizations/peerOrganizations/cgn.example.com/msp/tlscacerts"
  cp "${PWD}/organizations/fabric-ca/cgn/ca-cert.pem" "${PWD}/organizations/peerOrganizations/cgn.example.com/msp/tlscacerts/ca.crt"

  # Copy cgn's CA cert to cgn's /tlsca directory (for use by clients)
  mkdir -p "${PWD}/organizations/peerOrganizations/cgn.example.com/tlsca"
  cp "${PWD}/organizations/fabric-ca/cgn/ca-cert.pem" "${PWD}/organizations/peerOrganizations/cgn.example.com/tlsca/tlsca.cgn.example.com-cert.pem"

  # Copy cgn's CA cert to cgn's /ca directory (for use by clients)
  mkdir -p "${PWD}/organizations/peerOrganizations/cgn.example.com/ca"
  cp "${PWD}/organizations/fabric-ca/cgn/ca-cert.pem" "${PWD}/organizations/peerOrganizations/cgn.example.com/ca/ca.cgn.example.com-cert.pem"

  infoln "Registering peer0"
  set -x
  fabric-ca-client register --caname ca-cgn --id.name peer0 --id.secret peer0pw --id.type peer --tls.certfiles "${PWD}/organizations/fabric-ca/cgn/ca-cert.pem"
  { set +x; } 2>/dev/null

  infoln "Registering user"
  set -x
  fabric-ca-client register --caname ca-cgn --id.name user1 --id.secret user1pw --id.type client --tls.certfiles "${PWD}/organizations/fabric-ca/cgn/ca-cert.pem"
  { set +x; } 2>/dev/null

  infoln "Registering the org admin"
  set -x
  fabric-ca-client register --caname ca-cgn --id.name cgnadmin --id.secret cgnadminpw --id.type admin --tls.certfiles "${PWD}/organizations/fabric-ca/cgn/ca-cert.pem"
  { set +x; } 2>/dev/null

  infoln "Generating the peer0 msp"
  set -x
  fabric-ca-client enroll -u https://peer0:peer0pw@localhost:7054 --caname ca-cgn -M "${PWD}/organizations/peerOrganizations/cgn.example.com/peers/peer0.cgn.example.com/msp" --tls.certfiles "${PWD}/organizations/fabric-ca/cgn/ca-cert.pem"
  { set +x; } 2>/dev/null

  cp "${PWD}/organizations/peerOrganizations/cgn.example.com/msp/config.yaml" "${PWD}/organizations/peerOrganizations/cgn.example.com/peers/peer0.cgn.example.com/msp/config.yaml"

  infoln "Generating the peer0-tls certificates, use --csr.hosts to specify Subject Alternative Names"
  set -x
  fabric-ca-client enroll -u https://peer0:peer0pw@localhost:7054 --caname ca-cgn -M "${PWD}/organizations/peerOrganizations/cgn.example.com/peers/peer0.cgn.example.com/tls" --enrollment.profile tls --csr.hosts peer0.cgn.example.com --csr.hosts localhost --tls.certfiles "${PWD}/organizations/fabric-ca/cgn/ca-cert.pem"
  { set +x; } 2>/dev/null

  # Copy the tls CA cert, server cert, server keystore to well known file names in the peer's tls directory that are referenced by peer startup config
  cp "${PWD}/organizations/peerOrganizations/cgn.example.com/peers/peer0.cgn.example.com/tls/tlscacerts/"* "${PWD}/organizations/peerOrganizations/cgn.example.com/peers/peer0.cgn.example.com/tls/ca.crt"
  cp "${PWD}/organizations/peerOrganizations/cgn.example.com/peers/peer0.cgn.example.com/tls/signcerts/"* "${PWD}/organizations/peerOrganizations/cgn.example.com/peers/peer0.cgn.example.com/tls/server.crt"
  cp "${PWD}/organizations/peerOrganizations/cgn.example.com/peers/peer0.cgn.example.com/tls/keystore/"* "${PWD}/organizations/peerOrganizations/cgn.example.com/peers/peer0.cgn.example.com/tls/server.key"

  infoln "Generating the user msp"
  set -x
  fabric-ca-client enroll -u https://user1:user1pw@localhost:7054 --caname ca-cgn -M "${PWD}/organizations/peerOrganizations/cgn.example.com/users/User1@cgn.example.com/msp" --tls.certfiles "${PWD}/organizations/fabric-ca/cgn/ca-cert.pem"
  { set +x; } 2>/dev/null

  cp "${PWD}/organizations/peerOrganizations/cgn.example.com/msp/config.yaml" "${PWD}/organizations/peerOrganizations/cgn.example.com/users/User1@cgn.example.com/msp/config.yaml"

  infoln "Generating the org admin msp"
  set -x
  fabric-ca-client enroll -u https://cgnadmin:cgnadminpw@localhost:7054 --caname ca-cgn -M "${PWD}/organizations/peerOrganizations/cgn.example.com/users/Admin@cgn.example.com/msp" --tls.certfiles "${PWD}/organizations/fabric-ca/cgn/ca-cert.pem"
  { set +x; } 2>/dev/null

  cp "${PWD}/organizations/peerOrganizations/cgn.example.com/msp/config.yaml" "${PWD}/organizations/peerOrganizations/cgn.example.com/users/Admin@cgn.example.com/msp/config.yaml"
}

